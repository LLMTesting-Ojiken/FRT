CREATE DATABASE IF NOT EXISTS mydb;
USE mydb;
DROP TABLE IF EXISTS t1;
CREATE TABLE t1 (
    c0 DECIMAL NULL
);

INSERT INTO t1 (c0) VALUES (0);
INSERT INTO t1 (c0) VALUES (NULL);

-- Query 1 (baseline)
SELECT JSON_SET('{}', '$.k', t1.c0)
FROM t1
GROUP BY t1.c0;

-- Query 2 (with DISTINCT)
SELECT DISTINCT JSON_SET('{}', '$.k', t1.c0)
FROM t1
GROUP BY t1.c0;